    <pre style="font-size:18px;"><span style="text-align:left;">
    
    
<strong>CAMEL FESTIVAL</strong>
<img src="images/camelfestival.jpg" height="150" width="250" />
The Camel Festival is organized by the Department of Tourism of the Rajasthan Government in January<br />every year in Bikaner. The festival begins with a colorful procession of bedecked camels against the red<br />sandstone backdrop of the Junagarh Fort. Held is a tug-of-war contest,best breed competition, camel dance<br />and acrobatics among other things. The camels display amazing footwork, dancing gracefully to the directions<br />of their trainers.Bridal bridles be jeweled necks, jingling anklets and camel shadows, cast a spell<br />on the audience. In the evenings, is held a traditional rendezvous of renowned artistes and folk performers.
 
 
<strong>DESERT FESTIVAL</strong>
<img src="images/desert.jpg" height="150" width="250" />
This festival is also held in January- February in Jaisalmer. For three days, the otherwise barren land<br />of Jaisalmer comes to life and is clustered with hordes of colorfully dressed people.<br />Some chosen moments of the past and affluent culture are on display backed by high pitched music and folk<br />dances .The turban tying competition and Mr. Desert contest as also the camel races, add to the excitement<br />of the celebrations.


<strong>ELEPHANT FESTIVAL</strong>
<img src="images/elephant.jpg" height="150" width="200" />
The festival is held on the eve of Holi in Jaipur and has several interesting attractions like elephant polo.<br />The festival begins with a procession of elephants, camels, horses and folk dancers.The elephants<br />are decorated to the full with bright colours, saddle cloth and heavy jewellery. Female elephant are <br />made to wear anklets and prize is given for the most well-decorated elephant. Playing Holi on elephant <br />back is one of the major attractions.


<strong>DUSSHERA</strong>
<img src="images/dusherra.jpg" height="150" width="250" />
Dusshera is celebrated all over the country in different ways as also in Rajasthan.it celebrates<br />the triumph of good over evil the victory of Lord Rama over Ravana. The tale of Rama and Sita and the<br />battle fought between Lord Rama and Ravana are enacted on stae and it is called Ramlila. On the tenth day <br />of the festival, huge effigies of the tenheaded Ravana and his brother Kumbakaran, stuffed with thousands<br />of fire crackers, are set afire and the people<br />then begin to rejoice.


<strong>GANGUAR FAIR</strong>
<img src="images/gangaur.jpg" height="150" width="250" />
Geld all over Rajasthan, it is the most important fair of the state and is observed with fervour <br />and devotion.The idols of Issar and gangaur, manifestations of Shiva and Parvati, are worshipped by <br />women, particularly by those who are unmarred and pray for a consort. Women take out procession through the <br />streets carrying images of the divine couple. The festival is especially colorful in Jaipur, Udaipur.
 

<strong>TEEJ FESTIVAL</strong>
<img src="images/teej.jpg" height="150" width="250" />
Held during the monsoons, July-August Teej is also dedicated to Lord Shiva and Parvati and this<br /> time it is married women who pray for a happy and long married life. Though celebrations are held all over <br />the state,it is particularly colourful in Jaipur where a procesion winds its way for days through the<br />Old City.It is the festival of swings which are decorated with flowers and hung from trees. Young girls and <br />women dressed in green clothes sing songs in celebration of the advent of the monsoon. The Teej idol <br />is covered with a canopy whereas the Gangaur idol is open.


<strong>KITE FESTIVAL (held on 14th Jan of every year)</strong>
<img src="images/kite.jpg" height="150" width="250" />
A festival with a difference - as kites take to the sky all over Rajasthan. In the evening, kites with<br /> lights in them and fireworks brighten the skies above. The main celebrations are in Jaipur and Jodhpur. <br /> If you like kite flyiny you should be here.


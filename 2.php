<strong>Tour Itinerary and details</strong> 

<strong>DAY 1: ARRIVAL AT JAIPUR</strong>
Pickup from Jaipur Airport / Railway Station / Bus Stand by placard carrying driver and transfer to Hotel Umaid Bhawan or Umaid Mahal hotel. Later visit to the fabled Pink City, the old Jaipur.Drive through the Johari Bazaar to the Hawa Mahal (Palace of Winds). 
Visit the various museums inside the City Palace and the ancient observatory, Jantar Mantar.Pay a visit to the Govind Deoji Temple, housing the patron deity of Jaipur.
Evening free for your own activates like shopping, etc. Evening visit “Chokhi Dhani” for Rajasthani traditional dinner with dance and music. Overnight stay at hotel.

Tourist Places: Amber Fort, Jal Mahal, Hawa Mahal, City Palace, Jantar Mantar

<strong>DAY 2: JAIPUR - RANTHAMBHORE</strong>
Depart for Ranthambhore after breakfast. Visit Elephant Sculpture on the way. Check-in at hotel in Ranthambhore. After lunch visit to Ranthambhor National Park. (Entrance fee to National Park are extra). 

<strong>Tourist Places: Ranthambhore national Park.
DAY 3: RANTHAMBHORE - BHARATPUR</strong>
Morning visit to National Park - Project Tiger. After breakfast sightseeing of Ranthambhor Fort is optional (If guest agrees to reach by late evening at Bharatpur) Depart for Bharatpur.If time permits, a brief visit to the Keoladeo Ghana, Bird Sanctuary.
(Entry to, and guide fees in the park are extra) Check- in at Hotel in Bharatpur. 

<strong>Tourist Places: Keoladeo National Park, Bird Sanctuary.
DAY 4: BHARATPUR - JAIPUR</strong>
Morning visit to Keoladeo Ghana, Bird sanctuary. Depart for Jaipur after breakfast.
Visit to Abhaneri Step Wells on the way. Overnight stay at hotel in Jaipur.
<strong>DAY 5: DEPARTURE</strong>
On the last day of the trip, visit the magnificent Amber Palace. See the Largest Cannon on Wheels in the World, and old cannon foundry at Jaigarh. Visit to the Kanak Vrindavan Temple in the valley of flowers. Drop at Airport / Railway Station / Bus Stand in the evening.

